	public void setReplyToStatus(Status s)
	{
		if(s == null)
		{
			this.isReplyMode = false;
			this.replyToStatus = null;
			this.replyLabel.setVisible(false);
		}
		else
		{
			this.isReplyMode = true;
			this.replyToStatus = s;
			this.replyLabel.setVisible(true);
		}
	}

	public boolean isReplyMode()
	{
		return this.isReplyMode;
	}

	public void actionPerformed(ActionEvent e)
	{
		String cmd = e.getActionCommand();
		if("UPDATE_STATUS".equals(cmd))
		{
			Map map = Collections.synchronizedMap(new TreeMap());
			map.put("caller", e.getSource());
			map.put("async", true);
			ArrayList args = new ArrayList();
			StatusUpdate update = new StatusUpdate(txtTweet.getText());
			if(this.isReplyMode && this.replyToStatus != null)
			{
				update.setInReplyToStatusId(this.replyToStatus.getId());
			}
			args.add(update);
			map.put("arguments", args);
			logger.debug("Got action to perform");
			fireTwitzEvent(new TwitzEvent(this, TwitzEventType.valueOf(cmd),
						new java.util.Date().getTime(), map));
		}
	}

